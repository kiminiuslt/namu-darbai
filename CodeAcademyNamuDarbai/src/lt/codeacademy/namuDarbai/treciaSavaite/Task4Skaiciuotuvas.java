package lt.codeacademy.namuDarbai.treciaSavaite;

public class Task4Skaiciuotuvas {
	int sudetis;

	public int sudetis(int i, int j) {

		int sudetis = i + j;

		return sudetis;

	}

	public int atimtis(int a, int b) {

		int atimtis = a - b;

		return atimtis;
	}

	public int daugyba(int a, int b) {

		int daugyba = a * b;

		return daugyba;
	}

	public int dalyba(int a, int b) {

		int dalyba = a / b;

		return dalyba;
	}

	public int liekana(int a, int b) {
		int liekana = a % b;

		return liekana;
	}

}
